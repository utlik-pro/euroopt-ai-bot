"""Query normalizer: канонизация запроса перед RAG для повторяемости.

Цель: «магазины в Лиде» и «Евроопт Лида магазины» должны идти в RAG как
почти-одинаковый текст, чтобы получить одинаковый ранкинг и одинаковый ответ.

Алгоритм:
1. lowercase + нормализация пунктуации
2. Развёртывание синонимов и сокращений (ЛК → личный кабинет, Eplus → Еплюс)
3. Удаление стоп-слов («и», «в», «на», «у») в местах, где они не меняют смысл
4. Сортировка значимых токенов в стабильном порядке (после семантического match)
"""
from __future__ import annotations

import re

# Канонизация синонимов: разные написания → одно
SYNONYMS: dict[str, str] = {
    # Бренды (приводим к одной форме)
    r"\bевропоп\w*\b": "евроопт",
    r"\bевроопт\w*\b": "евроопт",
    r"\beuroopt\w*\b": "евроопт",
    r"\bевроторг\w*\b": "евроопт",
    r"\bгрошык\w*\b": "грошык",
    r"\bгрошик\w*\b": "грошык",
    r"\bхит\s+дискаунтер\b": "хит",
    r"\bхитдис\w*\b": "хит",
    r"\beplus\b": "еплюс",
    r"\be-plus\b": "еплюс",
    # «Е+» нельзя ловить через \b после +, т.к. + не word-char.
    # Используем lookahead на конец/нес-слово.
    r"\b[ее]\+(?=$|\s|[^\w+])": "еплюс",
    r"\bямигом\b": "ямигом",
    r"\bя\s+мигом\b": "ямигом",
    r"\bе-доставк\w*\b": "едоставка",
    r"\bе\s+доставк\w*\b": "едоставка",
    r"\be-dostavka\b": "едоставка",

    # Сокращения
    r"\bлк\b": "личный кабинет",
    r"\bл\.к\.\b": "личный кабинет",
    r"\bтц\b": "торговый центр",

    # Города (распространённые варианты)
    r"\bменск\b": "минск",
    r"\bмн\b": "минск",
    r"\bмск\b": "минск",  # на всякий случай (не путать с Москвой)
    # Простое стемминг для названий городов: «Лиде»/«Лиду»/«Лиды» → «Лида»,
    # «Минске»/«Минску» → «Минск», «Гродно» — без изменений (несклоняемое),
    # «Гомеле»/«Гомелю» → «Гомель» и т.п. Это нужно для повторяемости:
    # «магазины в Лиде» и «магазины Лида» должны идти в RAG как одно.
    r"\bлид[еуы]\b": "лида",
    r"\bминск[еу]\b": "минск",
    r"\bгроднен?[еу]?\b": "гродно",
    r"\bгомел[еюя]\b": "гомель",
    r"\bбрест[еу]\b": "брест",
    r"\bвитебск[еу]\b": "витебск",
    r"\bмогилёв[еу]\b": "могилёв",
    r"\bмогилев[еу]\b": "могилёв",

    # Карты / бонусы
    r"\bкарт[аыуи]\s+лояльн\w*\b": "карта еплюс",
    r"\bбонусы\s+еплюс\b": "бонусы",
    r"\bштрих[\s-]?код\w*\b": "штрихкод",
}

# Стоп-слова которые можно безопасно удалить (предлоги, частицы)
# Только если рядом есть значимое слово (т.е. не в начале единственного слова).
STOPWORDS = frozenset(
    [
        "в", "на", "у", "к", "из", "по", "при", "о", "об", "над",
        "под", "за", "для",
        "и", "или", "но", "а",
        "ли", "же", "бы",
        "как", "что", "это", "тут", "там",
    ]
)


def _apply_synonyms(text: str) -> str:
    for pattern, replacement in SYNONYMS.items():
        text = re.sub(pattern, replacement, text, flags=re.IGNORECASE)
    return text


def _strip_stopwords(text: str) -> str:
    tokens = text.split()
    if len(tokens) <= 2:
        # Слишком короткий запрос — не трогаем стоп-слова, иначе потеряем смысл
        return text
    out = [t for t in tokens if t not in STOPWORDS]
    if not out:
        return text  # все слова — стоп? возвращаем оригинал
    return " ".join(out)


def normalize_query(text: str, *, strip_stopwords: bool = True) -> str:
    """Канонизировать запрос для RAG. Не для отображения пользователю."""
    if not text:
        return ""
    low = text.lower().strip()
    # Убираем пунктуацию (кроме плюса и дефиса — нужны для «1+1», «е-доставка»)
    low = re.sub(r"[?!.,;:«»\"'()]", " ", low)
    low = re.sub(r"\s+", " ", low).strip()
    # Применяем синонимы
    low = _apply_synonyms(low)
    # Убираем стоп-слова
    if strip_stopwords:
        low = _strip_stopwords(low)
    return low.strip()


def canonicalize_for_cache(text: str) -> str:
    """Жёсткая нормализация для cache-ключа (отсортированные токены).

    Используется как ключ кэша: разные формулировки одного вопроса дают один
    и тот же ключ → один и тот же кэшированный ответ.
    """
    n = normalize_query(text, strip_stopwords=True)
    if not n:
        return ""
    tokens = sorted(n.split())
    return " ".join(tokens)
