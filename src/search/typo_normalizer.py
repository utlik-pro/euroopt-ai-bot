"""Лёгкая нормализация типовых опечаток перед RAG/Tavily поиском.

Применяется к user_message **только для построения search-query**, оригинальное
сообщение пользователя остаётся нетронутым и уходит в LLM как есть.

Задачи:
1. Растянутые символы («скидкиии» → «скидки»): пользователь выражает эмоции,
   но Tavily/RAG чувствительны к точному написанию.
2. Типовые опечатки в брендах (едаставка → едоставка, ямигомп → ямигом).

Чего НЕ делаем:
- Spell check на русском в общем случае — слишком рискованно и дорого.
- Замену словоформ/склонений — LLM и эмбеддинги справляются сами.
"""
from __future__ import annotations

import re


# 3+ одинаковые гласные подряд → 1 (скидкиии → скидки, дааа → да)
_REPEAT_VOWEL_RE = re.compile(r"([аеёиоуыэюяaeiouy])\1{2,}", re.IGNORECASE)
# 2+ одинаковые согласные в конце слова → 1 (скиидккк → скидк)
_REPEAT_CONS_END_RE = re.compile(
    r"([бвгджзйклмнпрстфхцчшщbcdfghjklmnpqrstvwxz])\1{2,}\b",
    re.IGNORECASE,
)

# Типовые опечатки в брендах (левая часть → правая)
BRAND_TYPO_MAP = {
    # Евроопт
    "евроопт": ["евроопд", "евроот", "еврооп ", "еўраопт"],
    # Е-доставка (латиница с ё-ы путаницы)
    "едоставка": ["едаставка", "едоставак", "е-даставка", "едаставку", "едаставки"],
    # Ямигом
    "ямигом": ["ямегом", "ямигомп", "ямиом"],
    # Грошык
    "грошык": ["грошик", "гошык"],
    # Хит Дискаунтер
    "хит дискаунтер": ["хит дискаунтэр", "хит дискаунтре"],
    # Еплюс
    "еплюс": ["еплус", "еплюз", "e-plus", "e-plyus"],
}


def _normalize_brand_typos(text: str) -> str:
    """Приводит типовые опечатки в брендах к канону."""
    low = text.lower()
    for canon, typos in BRAND_TYPO_MAP.items():
        for typo in typos:
            if typo in low:
                # Регистронезависимая замена, сохраняем регистр первой буквы замены
                pattern = re.compile(re.escape(typo), re.IGNORECASE)
                text = pattern.sub(canon, text)
                low = text.lower()
    return text


def normalize_typos(text: str) -> str:
    """Лёгкая нормализация для улучшения search-матчинга.

    НЕ меняет оригинальное сообщение пользователя. Используется ТОЛЬКО внутри
    pipeline для построения search_query, когда идём в RAG или Tavily.
    """
    if not text:
        return text
    # 1. Повторяющиеся гласные: «скидкиии» → «скидки»
    text = _REPEAT_VOWEL_RE.sub(r"\1", text)
    # 2. Повторяющиеся согласные в конце слова: «скидккк» → «скидк»
    text = _REPEAT_CONS_END_RE.sub(r"\1", text)
    # 3. Бренды
    text = _normalize_brand_typos(text)
    return text
