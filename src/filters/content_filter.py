import structlog

logger = structlog.get_logger()

BLOCKED_KEYWORDS = [
    # Политика — общие
    "политик", "политическ", "политзаключ", "президент",
    "политическая партия", "оппозиционная партия",
    "оппозиц", "санкци", "протест",
    "выборы", "выборов", "выбора", "фальсифик",
    "диктатур", "диктатор", "авторитарн", "тоталитарн",
    "демократи", "свобод слов", "критиковать власт",
    "революци", "майдан", "марш свобод",
    "режим",

    # Президенты и политики — РБ
    "лукашенк", "батька", "батьк",
    "тихановск", "бабарик", "латушк", "цепкал",
    "nexta", "нехта",

    # Президенты и политики — РФ/мир
    "путин", "зеленск", "трамп", "байден", "навальн",

    # Война и геополитика
    "война", "воевать", "воюет", "напала", "вторжени",
    "нато", "nato",

    # Репрессии и протесты РБ
    "омон", "окрестин", "политзек",
    "заключённ", "заключенн", "посадили",
    "управляет страной", "управляет беларус", "кто управля",
    "август 2020", "августе 2020", "свобод слова", "свободы слова", "вышли на улиц",

    # Религия
    # (религии раскоменчены — общие культурно-исторические факты разрешены, промпт сам отказывается на дискуссии)

    # Конкуренты
    "гиппо", "белмаркет", "соседи", "корон", "остров чистоты", "mile.by",
]

BLOCKED_PATTERNS_RE = [
    # Нецензурная лексика (включая "бля" без окончания)
    re.compile(r"(бля[дть]?|блять|ху[йяеи]|пизд|ебат|сук[аи]|мудак|пидор)", re.IGNORECASE),
    # "бог" как отдельное слово (не "богат", "много")
    re.compile(r"(?<!\w)бог[ауе]?(?!\w)", re.IGNORECASE),
    # (государство — убрано: слишком общее слово, не надо блокировать историю/географию)
]

POLITE_REFUSAL = (
    "Извините, я могу помочь вам с вопросами о наших магазинах, "
    "акциях, рецептах и услугах. Давайте поговорим о чём-то из этого! 😊\n\n"
    "Попробуйте спросить:\n"
    "• Какие сейчас акции?\n"
    "• Что приготовить на ужин?\n"
    "• Как работает доставка?"
)


SAFE_PHRASES = [
    "режим работы", "режим работает", "часы работы",
    "государственн", "государственный праздник",
    "партия товар", "партии товар",
    "демократичн", "демократичные цен",
    "революционн", "революционный вкус", "революционный нов",
]


def check_content(text: str) -> tuple[bool, str | None]:
    """Check if text contains blocked content.

    Returns:
        (is_allowed, refusal_message) - True if content is allowed, None if no refusal needed.
    """
    text_lower = text.lower()

    # Safe phrases — пропускаем если есть безопасный контекст
    for safe in SAFE_PHRASES:
        if safe in text_lower:
            return True, None

    # Keyword matching (case-insensitive substring)
    for keyword in BLOCKED_KEYWORDS:
        if keyword in text_lower:
            logger.info("content_filtered", matched=keyword, method="keyword")
            return False, POLITE_REFUSAL

    # Regex patterns (profanity, edge cases)
    for pattern in BLOCKED_PATTERNS_RE:
        match = pattern.search(text)
        if match:
            logger.info("content_filtered", matched=match.group(), method="regex")
            return False, POLITE_REFUSAL

    return True, None
