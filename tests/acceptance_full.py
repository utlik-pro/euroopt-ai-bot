async def main():
    print("=" * 70)
    print("ПОЛНЫЙ ПРИЁМОЧНЫЙ ТЕСТ — AI-помощник Евроопт")
    print(f"Дата: {datetime.now().strftime('%Y-%m-%d %H:%M')}")
    print(f"Тестов: {len(TESTS)}")
    print("=" * 70)

    pipeline = Pipeline()
    cats = {}
    total_p, total_f = 0, 0
    results = []
    start_all = time.monotonic()

    for i, t in enumerate(TESTS, 1):
        cat = t["cat"]
        if cat not in cats:
            cats[cat] = {"p": 0, "f": 0}
            print(f"\n━━━ {cat} ━━━")

        q = t["q"]
        start = time.monotonic()
        ok = True
        reason = ""
        answer = ""

        if t.get("block") is True:
            is_allowed, _ = check_content(q)
            ok = not is_allowed
            if not ok: reason = "Не заблокирован"
        elif t.get("block") is False:
            is_allowed, _ = check_content(q)
            if not is_allowed:
                ok = False
                reason = "Ложная блокировка"
            else:
                try:
                    uid = t.get("uid", 70000 + i)
                    answer = await pipeline.process(q, user_id=uid)
                    if "временная ошибка" in answer.lower():
                        ok = False
                        reason = "Ошибка LLM"
                    elif t.get("min_len") and len(answer) < t["min_len"]:
                        ok = False
                        reason = f"Короткий ({len(answer)} симв.)"
                except Exception as e:
                    ok = False
                    reason = str(e)[:100]
        else:
            try:
                uid = t.get("uid", 70000 + i)
                answer = await pipeline.process(q, user_id=uid)
                al = answer.lower()
                if "временная ошибка" in al:
                    ok = False
                    reason = "Ошибка LLM"
                elif t.get("kw") and not any(w in al for w in t["kw"]):
                    ok = False
                    reason = f"Нет слов: {t['kw'][:3]}"
                elif t.get("not_kw") and any(w in al for w in t["not_kw"]):
                    ok = False
                    reason = f"Есть запрещённое"
                elif t.get("min_len") and len(answer) < t["min_len"]:
                    ok = False
                    reason = f"Короткий ({len(answer)} симв.)"
            except Exception as e:
                ok = False
                reason = str(e)[:100]

        ms = int((time.monotonic() - start) * 1000)

        if ok:
            total_p += 1
            cats[cat]["p"] += 1
            print(f"  ✅ {q[:50]} ({ms}ms)")
        else:
            total_f += 1
            cats[cat]["f"] += 1
            print(f"  ❌ {q[:50]}")
            if reason: print(f"     → {reason}")
            if answer and not ok: print(f"     → {answer[:120]}")

        results.append({"q": q, "cat": cat, "ok": ok, "ms": ms, "reason": reason, "answer": answer[:200]})

    total_time = int(time.monotonic() - start_all)
    total = total_p + total_f
    pct = int(total_p / total * 100) if total else 0

    print(f"\n{'=' * 70}")
    print(f"{'Категория':<25} {'Pass':>6} {'Fail':>6}")
    print("-" * 40)
    for cat, d in cats.items():
        s = "✅" if d["f"] == 0 else "❌"
        print(f"{s} {cat:<23} {d['p']:>6} {d['f']:>6}")
    print("-" * 40)
    print(f"ВСЕГО: {total_p}/{total} ({pct}%) за {total_time} сек")

    with open("reports/acceptance_full.json", "w", encoding="utf-8") as f:
        json.dump({"date": datetime.now().isoformat(), "total": total, "passed": total_p,
                    "failed": total_f, "pct": pct, "time_sec": total_time,
                    "cats": {k: v for k, v in cats.items()}, "tests": results}, f, ensure_ascii=False, indent=2)

    with open("reports/acceptance_full.txt", "w", encoding="utf-8") as f:
        f.write(f"ПОЛНЫЙ ПРИЁМОЧНЫЙ ТЕСТ — AI-помощник Евроопт\nДата: {datetime.now().strftime('%d.%m.%Y %H:%M')}\n{'='*60}\n\n")
        f.write(f"РЕЗУЛЬТАТ: {total_p}/{total} ({pct}%)\n\n")
        for cat, d in cats.items():
            s = "PASS" if d["f"] == 0 else "FAIL"
            f.write(f"[{s}] {cat}: {d['p']}/{d['p']+d['f']}\n")
        f.write(f"\nДетали фейлов:\n{'='*60}\n")
        for r in results:
            if not r["ok"]:
                f.write(f"  [{r['cat']}] {r['q']}\n    → {r['reason']}\n")
                if r["answer"]: f.write(f"    → {r['answer'][:200]}\n")
                f.write("\n")

    print(f"\nОтчёты: reports/acceptance_full.json + .txt")

if __name__ == "__main__":
    asyncio.run(main())
