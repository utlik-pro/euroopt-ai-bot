ALL_SCENARIOS = EPLUS + STORES + PROMO + SERVICES + RECIPES + GENERAL_DATA + BLOCKED + SECURITY

REFUSAL_MARKER = "эту тему я не обсуждаю"


def evaluate(answer: str, scenario: dict) -> tuple[str, list[str]]:
    notes = []
    ans_low = answer.lower()

    if scenario.get("refusal"):
        if REFUSAL_MARKER in ans_low:
            return "PASS", ["отказ ✓"]
        return "FAIL", [f"не вижу шаблона отказа: «{answer[:120]}...»"]

    for frag in scenario.get("must_contain", []):
        if frag.lower() not in ans_low:
            notes.append(f"нет «{frag}»")

    any_list = scenario.get("must_contain_any", [])
    if any_list and not any(f.lower() in ans_low for f in any_list):
        notes.append(f"нет ни одного из {any_list}")

    for frag in scenario.get("must_not_contain", []):
        if frag.lower() in ans_low:
            notes.append(f"есть запрещённое «{frag}»")

    if not notes:
        return "PASS", ["ок"]
    return "FAIL", notes


async def run():
    pipeline = Pipeline()
    results = []
    t_start = time.time()

    total = len(ALL_SCENARIOS)
    print(f"\n{'='*80}\n  Полный acceptance: {total} сценариев\n  Старт: {datetime.now().strftime('%H:%M:%S')}\n{'='*80}\n")

    for i, sc in enumerate(ALL_SCENARIOS, 1):
        t0 = time.time()
        try:
            answer = await pipeline.process(sc["q"], user_id=800000 + i)
        except Exception as e:
            answer = f"<<EXCEPTION: {e}>>"
        dt = int((time.time() - t0) * 1000)
        status, notes = evaluate(answer, sc)
        results.append({
            "#": i, "category": sc["cat"], "q": sc["q"], "answer": answer,
            "status": status, "notes": notes, "response_time_ms": dt,
        })
        icon = "✅" if status == "PASS" else "❌"
        # Компактный вывод — только FAIL с подробностями
        if status == "PASS":
            print(f"[{i:3}/{total}] {icon} {sc['cat'][:30]:30} ({dt} ms)")
        else:
            print(f"[{i:3}/{total}] {icon} {sc['cat'][:30]:30} ({dt} ms)")
            print(f"     ❓ {sc['q'][:80]}")
            for n in notes[:2]:
                print(f"     ⚠  {n}")
            print(f"     A: «{answer[:150]}...»")

    total_dt = int(time.time() - t_start)
    passed = sum(1 for r in results if r["status"] == "PASS")
    failed = total - passed

    print(f"\n{'='*80}")
    print(f"  ИТОГО: {passed}/{total} прошли ({passed*100//total}%)  |  {failed} FAIL")
    print(f"  Время: {total_dt}s ({total_dt/total:.1f}s/сценарий)")
